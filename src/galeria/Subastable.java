package galeria;

/**
 * La interfaz Subastable define un método para determinar si un artículo es
 * subastable.
 */

public interface Subastable {

	/**
	 * Método para determinar si un artículo es subastable.
	 * 
	 * @return true si el artículo es subastable, false si no lo es.
	 */

	public boolean articuloSubastable();

}
